document.addEventListener('DOMContentLoaded', function () {
    const dropdowns = [
        { selected: 'dropdownSelected1', options: 'dropdownOptions1', flag: 'selectedFlag1', lang: 'selectedLanguage1' },
        { selected: 'dropdownSelected', options: 'dropdownOptions', flag: 'selectedFlag', lang: 'selectedLanguage' }
    ];

    dropdowns.forEach(dropdown => {
        const dropdownSelected = document.getElementById(dropdown.selected);
        const dropdownOptions = document.getElementById(dropdown.options);
        const selectedFlag = document.getElementById(dropdown.flag);
        const selectedLanguage = document.getElementById(dropdown.lang);

        // Toggle dropdown on click
        dropdownSelected.addEventListener('click', function (event) {
            dropdownOptions.classList.toggle('dropdown-open');
            event.stopPropagation(); // Prevent event from bubbling up
        });

        // Handle option selection inside the dropdown
        document.querySelectorAll(`#${dropdown.options} .dropdown-option`).forEach(option => {
            option.addEventListener('click', function () {
                const selectedFlagUrl = this.getAttribute('data-flag');
                const selectedText = this.textContent.trim();
                const selectedValue = this.getAttribute('data-value'); // Get the selected value

                // Update the selected flag and language text
                selectedFlag.src = selectedFlagUrl;
                selectedLanguage.textContent = selectedText;

                // Change the locale
                const url = new URL(window.location.href);
                url.searchParams.set('lang', selectedValue);
                window.history.replaceState(null, '', url.toString());

                setLocale(selectedValue);// Change language based on the selected value

                // Close dropdown after selection
                dropdownOptions.classList.remove('dropdown-open');
            });
        });

        // Close dropdown if clicked outside of it
        document.addEventListener('click', function (event) {
            if (!dropdownSelected.contains(event.target) && !dropdownOptions.contains(event.target)) {
                dropdownOptions.classList.remove('dropdown-open');
            }
        });
        initializeDropdownFromUrl(dropdown, dropdownOptions, selectedFlag, selectedLanguage);
    });
    function initializeDropdownFromUrl(dropdown, dropdownOptions, selectedFlag, selectedLanguage) {
        const urlParams = new URLSearchParams(window.location.search);
        const selectedLang = urlParams.get('lang');

        if (!selectedLang) return;

        const matchingOption = dropdownOptions.querySelector(`.dropdown-option[data-value="${selectedLang}"]`);
        if (matchingOption) {
            const selectedFlagUrl = matchingOption.getAttribute('data-flag');
            const selectedText = matchingOption.textContent.trim();

            selectedFlag.src = selectedFlagUrl;
            selectedLanguage.textContent = selectedText;
        }
    }
});
