// When the page content is ready...
document.addEventListener("DOMContentLoaded", () => {
    setLocale(locale); // Set the initial locale
    bindLocaleSwitcher(locale); // Bind the locale switcher
});

// Set the locale and update translations
function setLocale(newLocale) {
    if (translations[newLocale]) {
        locale = newLocale; // Update the locale dynamically
        updateContent();    // Update the text on the page
        // Adjust the text direction (LTR/RTL)
    }
}

// Update the page content with the selected locale
function updateContent() {
    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.getAttribute("data-i18n");
        element.textContent = translations[locale][key]; // Update content
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(elem => {
        const key = elem.getAttribute('data-i18n-placeholder');
        elem.placeholder = translations[locale][key] || key;
    });
    document.querySelectorAll(".error-msg[data-error-key]").forEach(elem => {
        const key = elem.getAttribute("data-error-key");
        elem.textContent = translations[locale][key] || key;
    });
}

// Update text direction based on locale (LTR or RTL)


// Bind the locale switcher dropdown to dynamically update locale
function bindLocaleSwitcher(initialValue) {
    const switcher = document.querySelector("[data-i18n-switcher]");
    switcher.value = initialValue;

    switcher.onchange = (e) => {
        setLocale(e.target.value); // Set the locale based on selected option
    };
}










